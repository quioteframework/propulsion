    /**
     * Add __call method to delegate unknown filterBy* calls to a foreign relation
     * if the table has a "delegate" behavior configured.
     * This allows tables to act as if they "extend" another table by delegating
     * column filters to the related table's query.
     *
     * @param string &$script The script will be modified in this method.
     */
    protected function addItemDelegationCallMethod(&$script): void
    {
        // Check if this table has a "delegate" behavior
        $delegateBehavior = null;
        $delegateToTable = null;

        foreach ($this->getTable()->getBehaviors() as $behavior) {
            if ($behavior->getName() === 'delegate') {
                $delegateBehavior = $behavior;
                // Get the "to" parameter which specifies which table to delegate to
                $delegateToTable = $behavior->getParameter('to');
                break;
            }
        }

        // Only add the __call method if there's a delegate behavior
        if ($delegateBehavior === null || !$delegateToTable) {
            return;
        }

        // Find the foreign key that references the delegate table
        $delegateForeignKey = null;
        foreach ($this->getTable()->getForeignKeys() as $fk) {
            if ($fk->getForeignTableName() === $delegateToTable) {
                $delegateForeignKey = $fk;
                break;
            }
        }

        if ($delegateForeignKey === null) {
            return;
        }

        // Get the phpName of the foreign key to use in useXXXQuery()
        $fkPhpName = $this->getFKPhpNameAffix($delegateForeignKey);
        $useMethodName = 'use' . $fkPhpName . 'Query';

        $script .= "
    /**
     * Delegate unknown method calls to the {$fkPhpName} relation query.
     * This allows filtering by {$fkPhpName} columns even though they don't exist on this table.
     * This is enabled by the 'delegate' behavior in the schema.
     *
     * @param string \$name The method name
     * @param array \$arguments The method arguments
     * @return mixed
     * @throws \\Propulsion\\Exception\\PropulsionException
     */
    public function __call(\$name, \$arguments)
    {
        // Try to handle it via the parent's __call first (for standard magic methods)
        try {
            return parent::__call(\$name, \$arguments);
        } catch (\\Propulsion\\Exception\\PropulsionException \$e) {
            // If it's a filterBy* method that failed, try delegating to the related table
            if (str_starts_with(\$name, 'filterBy')) {
                // Attempt to delegate to the related query
                try {
                    \$relatedQuery = \$this->{$useMethodName}();
                    \$result = call_user_func_array([\$relatedQuery, \$name], \$arguments);
                    return \$result->endUse();
                } catch (\\Exception \$delegateException) {
                    // If that also fails, throw the original exception
                    throw \$e;
                }
            }

            // If it's not a filterBy method, throw the original exception
            throw \$e;
        }
    }
";
    }

