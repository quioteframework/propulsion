    /**
     * Adds the filterByCrossFK method(s) with modern typing (FQCN param type hints).
     *
     * For backward compatibility, generates BOTH:
     * 1. Direct filter method: filterByUser($user)
     * 2. Cross-reference filter method: filterByUserViaCrossReference($user)
     *
     * @param      string &$script The script will be modified in this method.
     */
    protected function addFilterByCrossFK(&$script, ForeignKey $refFK, ForeignKey $crossFK): void
    {
        $queryClass = $this->getStubQueryBuilder()->getClassname();
        $crossRefTable = $crossFK->getTable();
        $foreignTable = $crossFK->getForeignTable();
        $fkPhpName = $foreignTable->getPhpName();
        $fkFQCN = '\\' . $this->getNewStubObjectBuilder($foreignTable)->getFullyQualifiedClassname();
        $crossTableName = $crossRefTable->getName();
        $relName = $this->getFKPhpNameAffix($crossFK, false) . 'ViaCrossReference';
        $directRelName = $this->getFKPhpNameAffix($crossFK, false);
        $objectName = '$' . $foreignTable->getStudlyPhpName();
        $relationName = $this->getRefFKPhpNameAffix($refFK, false);

        // Generate the DIRECT filter method (this is what generated getters/counters on the
        // related object call, e.g. BaseBookClubList::getBooks() calls filterByBookClubList())
        $script .= "
    /**
     * Filter the query by a related $fkPhpName object
     * via the cross-reference table
     *
     * @param     $fkFQCN $objectName the related object to use as filter
     * @param     string \$comparison Operator to use for the column comparison, defaults to Criteria::EQUAL
     *
     * @return    static The current query, for fluid interface
     */
    public function filterBy{$directRelName}($objectName, \$comparison = Criteria::EQUAL): $queryClass
    {
        return \$this
            ->use{$relationName}Query()
            ->filterBy{$directRelName}($objectName, \$comparison)
            ->endUse();
    }
";

        // Generate the explicit cross-reference filter method (an alias, for callers that
        // want to be unambiguous about which relation is being filtered on).
        $script .= "
    /**
     * Filter the query by a related $fkPhpName object
     * using the $crossTableName table as cross reference
     *
     * @param     $fkFQCN $objectName the related object to use as filter
     * @param     string \$comparison Operator to use for the column comparison, defaults to Criteria::EQUAL
     *
     * @return    static The current query, for fluid interface
     */
    public function filterBy{$relName}($objectName, \$comparison = Criteria::EQUAL): $queryClass
    {
        return \$this
            ->use{$relationName}Query()
            ->filterBy{$directRelName}($objectName, \$comparison)
            ->endUse();
    }
";
    }

