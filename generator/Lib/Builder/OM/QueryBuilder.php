    /**
     * Get modern PHP 8.4 type for a column
     */
    protected function getModernPhpType($column): string
    {
        if ($column->isTemporalType()) {
            $this->declareClass('\\DateTimeInterface');
            return $column->isNotNull() ? 'DateTimeInterface' : '?DateTimeInterface';
        }

        // Check the actual Propulsion type for better type hints
        // For BIGINT, use int since we're on 64-bit PHP 8.4 (handles up to 2^63-1)
        $propelType = $column->getType();

        if ($propelType === PropulsionTypes::BIGINT || $propelType === PropulsionTypes::INTEGER ||
            $propelType === PropulsionTypes::SMALLINT || $propelType === PropulsionTypes::TINYINT) {
            return $column->isNotNull() ? 'int' : '?int';
        }

        // Fall back to the PHP type mapping
        $phpType = $column->getPhpType();

        if ($phpType === 'DateTime') {
            $this->declareClass('\\DateTimeInterface');
            return $column->isNotNull() ? 'DateTimeInterface' : '?DateTimeInterface';
        }

        // Convert to modern nullable types where appropriate
        if ($column->isNotNull()) {
            return match($phpType) {
                'int' => 'int',
                'string' => 'string',
                'boolean' => 'bool',
                'double' => 'float',
                'resource' => 'resource',
                default => $phpType
            };
        }

        return match($phpType) {
            'int' => '?int',
            'string' => '?string',
            'boolean' => '?bool',
            'double' => '?float',
            'resource' => '?resource',
            default => '?' . $phpType
        };
    }

    /**
     * Override getUseStatements to provide PHP 8.4 compatible use statements with deduplication
     */
    public function getUseStatements($ignoredNamespace = null)
    {
        $script = '';
        $declaredClasses = $this->declaredClasses;
        unset($declaredClasses[$ignoredNamespace]);

        // Build a map of class names to their preferred fully qualified names.
        // These Propulsion\* core classes are aliased into the *global* namespace by
        // runtime/Lib/legacy-class-map.php, so a bare reference to e.g. `Criteria` only
        // resolves correctly on its own when the referencing class has no namespace of its
        // own -- inside a real `namespace Foo\Bar;` block, an unimported bare name resolves
        // relative to that namespace (`Foo\Bar\Criteria`), not globally, so a real `use`
        // import is still required there.
        $classMap = [];
        $preferredNamespaces = [
            'PropulsionException' => 'Propulsion\\Exception\\PropulsionException',
            'BasePeer' => 'Propulsion\\Util\\BasePeer',
            'Criteria' => 'Propulsion\\Query\\Criteria',
            'ModelCriteria' => 'Propulsion\\Query\\ModelCriteria',
            'ModelJoin' => 'Propulsion\\Query\\ModelJoin',
            'PropulsionPDO' => 'Propulsion\\Connection\\PropulsionPDO',
            'PropulsionCollection' => 'Propulsion\\Collection\\PropulsionCollection',
            'PropulsionObjectCollection' => 'Propulsion\\Collection\\PropulsionObjectCollection',
            'Propulsion' => 'Propulsion\\Propulsion',
            'BaseObject' => 'Propulsion\\OM\\BaseObject',
            'Persistent' => 'Propulsion\\OM\\Persistent'
        ];
        // True for the flat/legacy-style generation target (e.g. the bookstore fixture, or
        // any PropulsionQuickBuilder-built schema): no `namespace` statement of its own, so bare
        // core-class references already resolve via the global alias above with no import
        // needed -- and PropulsionQuickBuilder concatenates many such classes into a single
        // eval()'d script with no namespace block between them, so emitting the same
        // `use Fully\Qualified\Name;` once per class (as addClassBody()'s FQCN
        // declareClass() calls would otherwise cause here) is a fatal redeclaration.
        $isFlat = !$this->getNamespace();

        // Collect all classes and prefer properly namespaced versions
        foreach ($declaredClasses as $namespace => $classes) {
            foreach ($classes as $class) {
                $fullName = $namespace ? $namespace . '\\' . $class : $class;

                if ($isFlat && isset($preferredNamespaces[$class])
                    && ($namespace === '' || $fullName === $preferredNamespaces[$class])) {
                    // Flat target referencing a globally-aliased core class: no import needed.
                    continue;
                }

                if (!$isFlat && $namespace === '' && isset($preferredNamespaces[$class])) {
                    // Namespaced target with a legacy bare declare of a core class: import its
                    // real FQCN, since a bare reference here would resolve relative to this
                    // class's own namespace instead.
                    $fullName = $preferredNamespaces[$class];
                }

                // Use the class name as the key for deduplication
                $classMap[$class] = $fullName;
            }
        }

        // Sort by fully qualified name for consistency
        asort($classMap);

        foreach ($classMap as $className => $fullName) {
            $script .= sprintf("use %s;\n", $fullName);
        }

        return $script;
    }

    /**
     * Specifies the methods that are added as part of the stub object class.
     *
     * By default there are no methods for the empty stub classes; override this method
     * if you want to change that behavior.
     *
     * @see        AbstractObjectBuilder::addClassBody()
     */
    protected function addClassBody(&$script): void
    {
        // Declare essential classes for Base*Query classes with proper namespaces
        $this->declareClass('Propulsion\Query\ModelCriteria');
        $this->declareClass('Propulsion\Connection\PropulsionPDO');
        $this->declareClass('Propulsion\Query\Criteria');
        $this->declareClass('Propulsion\Exception\PropulsionException');
        $this->declareClass('Propulsion\Collection\PropulsionCollection');
        $this->declareClass('Propulsion\Collection\PropulsionObjectCollection');
        $this->declareClass('Propulsion\Query\ModelJoin');
        $this->declareClass('Propulsion\Propulsion');

        // Declare related builders for type hints and relationships
        $this->declareClassFromBuilder($this->getStubPeerBuilder());
        $this->declareClassFromBuilder($this->getStubObjectBuilder());

        // namespaces
        $this->declareClasses('ModelCriteria', 'Criteria', 'ModelJoin');
        $this->declareClassFromBuilder($this->getStubQueryBuilder());
        $this->declareClassFromBuilder($this->getStubPeerBuilder());

        // apply behaviors
        $this->applyBehaviorModifier('queryAttributes', $script, "	");
        $this->addConstructor($script);
        $this->addFactory($script);
        $this->addFindPk($script);
        $this->addFindPks($script);
        $this->addFilterByPrimaryKey($script);
        $this->addFilterByPrimaryKeys($script);
        foreach ($this->getTable()->getColumns() as $col) {
            $this->addFilterByCol($script, $col);
            if ($col->getType() === PropulsionTypes::PHP_ARRAY && $col->isNamePlural()) {
                $this->addFilterByArrayCol($script, $col);
            }
        }
        foreach ($this->getTable()->getForeignKeys() as $fk) {
            $this->addFilterByFK($script, $fk);
            $this->addJoinFk($script, $fk);
            $this->addUseFKQuery($script, $fk);
        }
        foreach ($this->getTable()->getReferrers() as $refFK) {
            $this->addFilterByRefFK($script, $refFK);
            $this->addJoinRefFk($script, $refFK);
            $this->addUseRefFKQuery($script, $refFK);
        }
        foreach ($this->getTable()->getCrossFks() as $fkList) {
            list($refFK, $crossFK) = $fkList;
            $this->addFilterByCrossFK($script, $refFK, $crossFK);
        }
        $this->addPrune($script);
        $this->addBasePreSelect($script);
        $this->addBasePreDelete($script);
        $this->addBasePostDelete($script);
        $this->addBasePreUpdate($script);
        $this->addBasePostUpdate($script);
        // apply behaviors
        $this->applyBehaviorModifier('queryMethods', $script, "	");

        // Add __call delegation for tables that have a foreign key to Item
        $this->addItemDelegationCallMethod($script);
    }

