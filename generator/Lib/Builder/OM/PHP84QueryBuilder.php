    /**
     * Override getUseStatements to provide PHP 8.4 compatible use statements with deduplication
     */
    public function getUseStatements($ignoredNamespace = null)
    {
        $script = '';
        $declaredClasses = $this->declaredClasses;
        unset($declaredClasses[$ignoredNamespace]);
        
        // Build a map of class names to their preferred fully qualified names
        $classMap = [];
        $preferredNamespaces = [
            'PropelException' => 'Propel\\Exception\\PropelException',
            'BasePeer' => 'Propel\\Util\\BasePeer',
            'Criteria' => 'Propel\\Query\\Criteria',
            'ModelCriteria' => 'Propel\\Query\\ModelCriteria',
            'ModelJoin' => 'Propel\\Query\\ModelJoin',
            'PropelPDO' => 'Propel\\Connection\\PropelPDO',
            'PropelCollection' => 'Propel\\Collection\\PropelCollection',
            'PropelObjectCollection' => 'Propel\\Collection\\PropelObjectCollection',
            'Propel' => 'Propel\\Propel',
            'BaseObject' => 'Propel\\OM\\BaseObject',
            'Persistent' => 'Propel\\OM\\Persistent'
        ];
        
        // Collect all classes and prefer properly namespaced versions
        foreach ($declaredClasses as $namespace => $classes) {
            foreach ($classes as $class) {
                $fullName = $namespace ? $namespace . '\\' . $class : $class;
                
                // Skip root namespace classes that have preferred namespaced versions
                if ($namespace === '' && isset($preferredNamespaces[$class])) {
                    continue;
                }
                
                // Use the class name as the key for deduplication
                $classMap[$class] = $fullName;
            }
        }
        
        // Sort by fully qualified name for consistency
        asort($classMap);
        
        foreach ($classMap as $className => $fullName) {
            if (strpos($fullName, '\\') === 0) {
                // Global namespace class (starts with \)
                $script .= sprintf("use %s;\n", $fullName);
            } else {
                // Namespaced class
                $script .= sprintf("use %s;\n", $fullName);
            }
        }
        
        return $script;
    }

    /**
     * Override the addFilterByCrossFK method to generate both direct and cross-reference filter methods
     * 
     * For backward compatibility, we need to generate BOTH:
     * 1. Direct filter method: filterByUser($user) 
     * 2. Cross-reference filter method: filterByUserViaCrossReference($user)
     */
    protected function addFilterByCrossFK(&$script, ForeignKey $refFK, ForeignKey $crossFK): void
    {
        $queryClass = $this->getStubQueryBuilder()->getClassname();
        $crossRefTable = $crossFK->getTable();
        $foreignTable = $crossFK->getForeignTable();
        $fkPhpName = $foreignTable->getPhpName();
        $fkFQCN = '\\' . $this->getNewStubObjectBuilder($foreignTable)->getFullyQualifiedClassname();
        $crossTableName = $crossRefTable->getName();
        $relName = $this->getFKPhpNameAffix($crossFK, false) . 'ViaCrossReference';
        $directRelName = $this->getFKPhpNameAffix($crossFK, false);
        $objectName = '$' . $foreignTable->getStudlyPhpName();
        $relationName = $this->getRefFKPhpNameAffix($refFK, false);

        // Generate the DIRECT filter method for backward compatibility
        $script .= "
    /**
     * Filter the query by a related $fkPhpName object
     * via the cross-reference table
     *
     * @param     $fkFQCN $objectName the related object to use as filter
     * @param     string \$comparison Operator to use for the column comparison, defaults to Criteria::EQUAL
     *
     * @return    $queryClass The current query, for fluid interface
     */
    public function filterBy{$directRelName}($objectName, \$comparison = Criteria::EQUAL): $queryClass
    {
        return \$this
            ->use{$relationName}Query()
            ->filterBy{$directRelName}($objectName, \$comparison)
            ->endUse();
    }
";

        // Generate the EXPLICIT cross-reference filter method
        $script .= "
    /**
     * Filter the query by a related $fkPhpName object
     * using the $crossTableName table as cross reference
     *
     * @param     $fkFQCN $objectName the related object to use as filter
     * @param     string \$comparison Operator to use for the column comparison, defaults to Criteria::EQUAL
     *
     * @return    $queryClass The current query, for fluid interface
     */
    public function filterBy{$relName}($objectName, \$comparison = Criteria::EQUAL): $queryClass
    {
        return \$this
            ->use{$relationName}Query()
            ->filterBy{$directRelName}($objectName, \$comparison)
            ->endUse();
    }
";
    }

    /**
     * Gets the appropriate PHP 8.4 type hint for filterBy method parameters
     * @param Column $col
     * @return string
     */
    protected function getFilterByParamTypeHint($col): string
    {
        // Check for temporal types first
        if ($col->isTemporalType()) {
            // Ensure DateTimeInterface is imported
            $this->declareClass('\\DateTimeInterface');
            // Accept DateTimeInterface, ints (timestamps), strings, arrays (ranges) or null
            return 'DateTimeInterface|int|string|array|null';
        }
        
        // Check the actual Propel type for better type hints
        // For BIGINT, use int since we're on 64-bit PHP 8.4
        $propelType = $col->getType();
        
        if ($propelType === PropelTypes::BIGINT || $propelType === PropelTypes::INTEGER || 
            $propelType === PropelTypes::SMALLINT || $propelType === PropelTypes::TINYINT) {
            // numeric-like columns accept an int, an array for IN/range queries, or null
            return 'int|array|null';
        }

        if ($propelType === PropelTypes::BOOLEAN) {
            // booleans can be passed as bool or common string representations
            return 'bool|string|null';
        }

        if ($propelType === PropelTypes::PHP_ARRAY) {
            // explicit PHP array column
            return 'array|null';
        }

        if ($col->isTextType()) {
            // text columns accept string or array (IN queries), and may be null
            return 'string|array|null';
        }

        // Fallback: convert the modern PHP type to an explicit union that allows null
        $phpType = $this->getModernPhpType($col);
        // getModernPhpType may return a nullable form like '?string' or '?int'
        if (str_starts_with($phpType, '?')) {
            $base = substr($phpType, 1);
            return $base . '|null';
        }
        // non-null form (e.g. 'int' or 'DateTimeInterface') -> allow null as filter param
        return $phpType . '|null';
    }

    /**
     * Override to add PHP 8.4 type hints to filterBy methods
     */
    protected function addFilterByCol(&$script, $col)
    {
        $colPhpName = $col->getPhpName();
        $colName = $col->getName();
        $variableName = $col->getStudlyPhpName();
        $qualifiedName = $this->getColumnConstant($col);
        $typeHint = $this->getFilterByParamTypeHint($col);
        
        $script .= "
    /**
     * Filter the query on the $colName column
     *";
        if ($col->isNumericType()) {
            $script .= "
     * Example usage:
     * <code>
     * \$query->filterBy$colPhpName(1234); // WHERE $colName = 1234
     * \$query->filterBy$colPhpName(array(12, 34)); // WHERE $colName IN (12, 34)
     * \$query->filterBy$colPhpName(array('min' => 12)); // WHERE $colName > 12
     * </code>";
            if ($col->isForeignKey()) {
                foreach ($col->getForeignKeys() as $fk) {
                    $script .= "
     *
     * @see       filterBy" . $this->getFKPhpNameAffix($fk) . "()";
                }
            }
            $script .= "
     *
         * @param     " . $typeHint . " \$$variableName The value to use as filter.
     *              Use scalar values for equality.
     *              Use array values for in_array() equivalent.
     *              Use associative array('min' => \$minValue, 'max' => \$maxValue) for intervals.";
        } elseif ($col->isTemporalType()) {
            $script .= "
     * Example usage:
     * <code>
     * \$query->filterBy$colPhpName('2011-03-14'); // WHERE $colName = '2011-03-14'
     * \$query->filterBy$colPhpName('now'); // WHERE $colName = '2011-03-14'
     * \$query->filterBy$colPhpName(array('max' => 'yesterday')); // WHERE $colName > '2011-03-13'
     * </code>
     *
         * @param     " . $typeHint . " \$$variableName The value to use as filter.
     *              Values can be integers (unix timestamps), DateTime objects, or strings.
     *              Empty strings are treated as NULL.
     *              Use scalar values for equality.
     *              Use array values for in_array() equivalent.
     *              Use associative array('min' => \$minValue, 'max' => \$maxValue) for intervals.";
        } elseif ($col->getType() == PropelTypes::PHP_ARRAY) {
            $script .= "
         * @param     " . $typeHint . " \$$variableName The values to use as filter.";
        } elseif ($col->isTextType()) {
            $script .= "
     * Example usage:
     * <code>
     * \$query->filterBy$colPhpName('fooValue');   // WHERE $colName = 'fooValue'
     * \$query->filterBy$colPhpName('%fooValue%'); // WHERE $colName LIKE '%fooValue%'
     * </code>
     *
         * @param     " . $typeHint . " \$$variableName The value to use as filter.
     *              Accepts wildcards (* and % trigger a LIKE).
     *              Can be an array for IN queries.";
        } elseif ($col->isBooleanType()) {
            $script .= "
     * Example usage:
     * <code>
     * \$query->filterBy$colPhpName(true); // WHERE $colName = true
     * \$query->filterBy$colPhpName('yes'); // WHERE $colName = true
     * </code>
     *
         * @param     " . $typeHint . " \$$variableName The value to use as filter.
     *              Non-boolean arguments are converted using the following rules:
     *                * 1, '1', 'true',  'on',  and 'yes' are converted to boolean true
     *                * 0, '0', 'false', 'off', and 'no'  are converted to boolean false
     *              Check on string values is case insensitive (so 'FaLsE' is seen as 'false').";
        } else {
            $script .= "
         * @param     " . $typeHint . " \$$variableName The value to use as filter";
        }
        $script .= "
     * @param     string|null \$comparison Operator to use for the column comparison, defaults to Criteria::EQUAL
     *
     * @return    static The current query, for fluid interface
     */
    public function filterBy$colPhpName($typeHint \$$variableName = null, ?string \$comparison = null): static
    {";
        if ($col->isPrimaryKey() && ($col->getType() == PropelTypes::INTEGER || $col->getType() == PropelTypes::BIGINT)) {
            $script .= "
        if (is_array(\$$variableName) && null === \$comparison) {
            \$comparison = Criteria::IN;
        }";
        } elseif ($col->isNumericType() || $col->isTemporalType()) {
            $script .= "
        if (is_array(\$$variableName)) {
            \$useMinMax = false;
            if (isset(\${$variableName}['min'])) {
                \$this->addUsingAlias($qualifiedName, \${$variableName}['min'], Criteria::GREATER_EQUAL);
                \$useMinMax = true;
            }
            if (isset(\${$variableName}['max'])) {
                \$this->addUsingAlias($qualifiedName, \${$variableName}['max'], Criteria::LESS_EQUAL);
                \$useMinMax = true;
            }
            if (\$useMinMax) {
                return \$this;
            }
            if (null === \$comparison) {
                \$comparison = Criteria::IN;
            }
        }";
        } elseif ($col->getType() == PropelTypes::PHP_ARRAY) {
            $script .= "
        \$key = \$this->getAliasedColName($qualifiedName);
        if (null === \$comparison || \$comparison == Criteria::CONTAINS_ALL) {
            foreach ((array) \$$variableName as \$value) {
                \$value = '%| ' . \$value . ' |%';
                if (\$this->containsKey(\$key)) {
                    \$this->addAnd(\$key, \$value, Criteria::LIKE);
                } else {
                    \$this->add(\$key, \$value, Criteria::LIKE);
                }
            }

            return \$this;
        } elseif (\$comparison == Criteria::CONTAINS_SOME) {
            foreach ((array) \$$variableName as \$value) {
                \$value = '%| ' . \$value . ' |%';
                if (\$this->containsKey(\$key)) {
                    \$this->addOr(\$key, \$value, Criteria::LIKE);
                } else {
                    \$this->add(\$key, \$value, Criteria::LIKE);
                }
            }

            return \$this;
        } elseif (\$comparison == Criteria::CONTAINS_NONE) {
            foreach ((array) \$$variableName as \$value) {
                \$value = '%| ' . \$value . ' |%';
                if (\$this->containsKey(\$key)) {
                    \$this->addAnd(\$key, \$value, Criteria::NOT_LIKE);
                } else {
                    \$this->add(\$key, \$value, Criteria::NOT_LIKE);
                }
            }
            \$this->addOr(\$key, null, Criteria::ISNULL);

            return \$this;
        }";
        } elseif ($col->isTextType()) {
            $script .= "
        if (null === \$comparison) {
            if (is_array(\$$variableName)) {
                \$comparison = Criteria::IN;
            } elseif (\$$variableName !== null && preg_match('/[\%\*]/', \$$variableName)) {
                \$$variableName = str_replace('*', '%', \$$variableName);
                \$comparison = Criteria::LIKE;
            }
        }";
        } elseif ($col->isBooleanType()) {
            $script .= "
        if (is_string(\$$variableName)) {
            \$boolVal = in_array(strtolower(\$$variableName), array('false', 'off', '-', 'no', 'n', '0', '')) ? false : true;
            \$$variableName = \$boolVal;
        }";
        }
        $script .= "

        return \$this->addUsingAlias($qualifiedName, \$$variableName, \$comparison);
    }
";
    } // addFilterByCol
    
    /**
     * Add __call method to delegate unknown filterBy* calls to a foreign relation
     * if the table has a "delegate" behavior configured.
     * This allows tables to act as if they "extend" another table by delegating
     * column filters to the related table's query.
     *
     * @param string &$script The script will be modified in this method.
     */
    protected function addItemDelegationCallMethod(&$script): void
    {
        // Check if this table has a "delegate" behavior
        $delegateBehavior = null;
        $delegateToTable = null;
        
        foreach ($this->getTable()->getBehaviors() as $behavior) {
            if ($behavior->getName() === 'delegate') {
                $delegateBehavior = $behavior;
                // Get the "to" parameter which specifies which table to delegate to
                $delegateToTable = $behavior->getParameter('to');
                break;
            }
        }
        
        // Only add the __call method if there's a delegate behavior
        if ($delegateBehavior === null || !$delegateToTable) {
            return;
        }
        
        // Find the foreign key that references the delegate table
        $delegateForeignKey = null;
        foreach ($this->getTable()->getForeignKeys() as $fk) {
            if ($fk->getForeignTableName() === $delegateToTable) {
                $delegateForeignKey = $fk;
                break;
            }
        }
        
        if ($delegateForeignKey === null) {
            return;
        }
        
        // Get the phpName of the foreign key to use in useXXXQuery()
        $fkPhpName = $this->getFKPhpNameAffix($delegateForeignKey);
        $useMethodName = 'use' . $fkPhpName . 'Query';
        
        $script .= "
    /**
     * Delegate unknown method calls to the {$fkPhpName} relation query.
     * This allows filtering by {$fkPhpName} columns even though they don't exist on this table.
     * This is enabled by the 'delegate' behavior in the schema.
     *
     * @param string \$name The method name
     * @param array \$arguments The method arguments
     * @return mixed
     * @throws \\Propel\\Exception\\PropelException
     */
    public function __call(\$name, \$arguments)
    {
        // Try to handle it via the parent's __call first (for standard magic methods)
        try {
            return parent::__call(\$name, \$arguments);
        } catch (\\Propel\\Exception\\PropelException \$e) {
            // If it's a filterBy* method that failed, try delegating to the related table
            if (str_starts_with(\$name, 'filterBy')) {
                // Attempt to delegate to the related query
                try {
                    \$relatedQuery = \$this->{$useMethodName}();
                    \$result = call_user_func_array([\$relatedQuery, \$name], \$arguments);
                    return \$result->endUse();
                } catch (\\Exception \$delegateException) {
                    // If that also fails, throw the original exception
                    throw \$e;
                }
            }
            
            // If it's not a filterBy method, throw the original exception
            throw \$e;
        }
    }
";
    }
