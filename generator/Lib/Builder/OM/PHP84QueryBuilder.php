    /**
     * Adds findPk method with modern typing.
     * @param      string &$script The script will be modified in this method.
     */
    protected function addFindPk(&$script): void
    {
        $pks = $this->getTable()->getPrimaryKey();
        if (count($pks) == 1) {
            $pk = $pks[0];
            $script .= "
    /**
     * Find object by primary key.
     * Propel uses the instance pool to skip the database if the object exists.
     * Go fast if the query is untouched.
     *
     * @param      mixed \$key Primary key to use for the query
     * @param      ?PropelPDO \$con An optional connection object
     *
     * @return     " . $this->getObjectBuilder()->getClassname() . "|" . $this->getObjectBuilder()->getClassname() . "[]|null|mixed The result, formatted by the current formatter
     */
    public function findPk(mixed \$key, ?PropelPDO \$con = null): mixed
    {
        if (\$key === null) {
            return null;
        }
        if ((null !== (\$obj = " . $this->getPeerBuilder()->getClassname() . "::getInstanceFromPool((string) \$key))) && !\$this->formatter) {
            // the object is alredy in the instance pool
            return \$obj;
        }
        if (\$con === null) {
            \$con = Propel::getConnection(" . $this->getPeerBuilder()->getClassname() . "::DATABASE_NAME, Propel::CONNECTION_READ);
        }";
        
        // Set up pool key hash parameters
        if (count($pks) === 1) {
            $poolKeyHashParams = '$key';
        } else {
            $poolKeyHashParams = array();
            for ($i = 0, $count = count($pks); $i < $count; $i++) {
                $poolKeyHashParams[]= '$key[' . $i . ']';
            }
        }
        
        $script .= "
        if ((null !== (\$obj = " . $this->getPeerBuilder()->getClassname() . "::getInstanceFromPool(".$this->getPeerBuilder()->getInstancePoolKeySnippet($poolKeyHashParams)."))) && \$this->getFormatter()->isObjectFormatter()) {
            // the object is already in the instance pool
            return \$obj;
        } else {
            // the object has not been requested yet, or the formatter is not an object formatter
            \$criteria = \$this->isKeepQuery() ? clone \$this : \$this;
            \$stmt = \$criteria
                ->filterByPrimaryKey(\$key)
                ->getSelectStatement(\$con);
            return \$criteria->getFormatter()->init(\$criteria)->formatOne(\$stmt);
        }
    }
";
        } else {
            // Composite primary key
            $pkParams = [];
            foreach ($pks as $pk) {
                $pkParams[] = 'mixed $' . $pk->getName();
            }
            $pkParamsStr = implode(', ', $pkParams);
            
            $script .= "
    /**
     * Find object by primary key using raw SQL to go fast.
     * Bypass doSelect() and the object formatter by using generated code.
     *
     * @param      $pkParamsStr Primary keys to use for the query
     * @param      ?PropelPDO \$con A connection object
     *
     * @return     ?" . $this->getObjectBuilder()->getClassname() . " A model object, or null if the key is not found
     */
    protected function findPkSimple($pkParamsStr, ?PropelPDO \$con): ?" . $this->getObjectBuilder()->getClassname() . "
    {
        // Implementation would be generated here
        throw new PropelException('Composite primary key not yet implemented for PHP84QueryBuilder');
    }
";
        }
        
        // Add findPkSimple method for single primary key tables
        if (count($pks) == 1) {
            $this->addFindPkSimple($script);
        }
    }

